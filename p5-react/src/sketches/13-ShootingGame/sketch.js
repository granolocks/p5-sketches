var Barrier = function(p, x, y, size=100){
  this.xy = p.createVector(x,y);
  this.height = size
  this.width = size

  this.display = function(){
    p.noStroke();
    p.fill(151)
    p.rect(this.xy.x, this.xy.y, this.width, this.height);
  }
}

var Bullet = function(p,x,y,v) {
  this.xy = p.createVector(x,y);
  this.velocity = v;

  this.update = function(){
    this.xy.add(this.velocity)
  }

  this.display = function(){
    p.fill('orange')
    p.stroke('orange')
    p.ellipse(this.xy.x, this.xy.y, 2, 2)
  }

  this.collide = function(barrier) {
    if (
      this.xy.x + this.velocity.x > barrier.xy.x &&
      this.xy.x + this.velocity.x < barrier.xy.x + barrier.width &&
      this.xy.y + this.velocity.y > barrier.xy.y &&
      this.xy.y + this.velocity.y < barrier.xy.y + barrier.height
    ) {
      let bVec = p.createVector(barrier.xy.x+(barrier.width/2), barrier.xy.y+(barrier.height/2))
      bVec.sub(this.xy)

      let bAngle = p.abs(p.atan(bVec.y/bVec.x))

      if (bAngle >= p.PI/4 && bAngle < 3*p.PI/4) {
        // bottom
        this.velocity.y *= -1
        // this.location.y += 1
      } else if (bAngle >= 3*p.PI/4 && bAngle < 5*p.PI/4) {
        // left side
        this.velocity.x *= -1
        // this.location.x -= 1
      } else if (bAngle >= 5*p.PI/4 && bAngle <= 7*p.PI/4) {
        // top
        this.velocity.y *= -1
        // this.location.y -= 1
      } else  {
        //right side
        this.velocity.x *= -1
        // this.location.x += 1
      }
    }
  }
}


var Character = function(p, mass=5){
  this.location = p.createVector(p.width/2, p.height/2);
  this.velocity = p.createVector(0,0);
  this.acceleration = p.createVector(0,0);

  this.mass = mass;
  this.size = mass*5;
  this.radius = this.size/2;

  this.color = 'lightblue'
  this.borderColor = 'green'

  this.angle = p.createVector(1,0);
  this.score = 0;

  this.maxAmmo = 100;

  this.x = function() {return this.location.x};
  this.y = function() {return this.location.y};

  this.display = function() {
    if(this.remainingAmmo() < 30){
      this.borderColor = ('red')
    } else if(this.remainingAmmo() < 70){
      this.borderColor = ('yellow')
    } else {
      this.borderColor = ('green')
    }

    p.strokeWeight(2)
    p.stroke(this.borderColor)
    p.fill(this.color)
    p.ellipse(this.x(), this.y(), this.size)
    p.line(
      this.x() + this.angle.x * this.size/1.2,
      this.y() + this.angle.y * this.size/1.2,
      this.x() + this.angle.x * this.size/2,
      this.y() + this.angle.y * this.size/2
    );
    p.fill(this.borderColor)
    p.stroke(this.borderColor)
    p.text(this.score,this.x() - 4, this.y()+4)
    p.text(this.remainingAmmo(),this.x() - 9, this.y()+34)

  }


  this.applyForce = function(forceV){
    let tempV = p.createVector(forceV.x, forceV.y);
    tempV.div(this.mass);
    this.velocity.add(tempV)
  }

  this.checkEdges = function(x_low, x_high, y_low, y_high) {
    if (this.x() >= x_high - (this.size/2 + 1) ) {
      this.velocity.x *= -0.8;
      this.location.x = x_high - (this.size / 2) - 1;
    }
    if (this.x() <= x_low + (this.size/2 + 1) ) {
      this.velocity.x *= -0.8;
      this.location.x = x_low + (this.size / 2) + 1;
    }

    if (this.y() >= y_high - (this.size/2 + 1)) {
      this.velocity.y *= -0.8
      this.location.y = y_high - (this.size / 2) - 1;
    }
    if (this.y() <= y_low + (this.size/2 + 1)) {
      this.velocity.y *= -0.8
      this.location.y = y_low + (this.size / 2) + 1;
    }
  }

  this.remainingAmmo = function() {
    return this.maxAmmo - p.bullets.length
  }

  this.shoot = function(){
    this.remainingAmmo() > 0 &&
    p.bullets.push(
      new Bullet(p,
        this.x() + this.angle.x * this.size/1.2,
        this.y() + this.angle.y * this.size/1.2,
        p.createVector(this.angle.x, this.angle.y)
      )
    )
  }

  this.collide = function(barrier) {
    // check 50 points around the circle,
    // if any are inside the barrier reflect the velocity
    let done = false;
    for (var i = 0; i < 10; i ++){
      if (!done){
        let a = p.TWO_PI/10 * i
        let px = (p.cos(a) * this.radius) + this.x();
        let py = (p.sin(a) * this.radius) + this.y();

        // let pt = p.createVector(px + this.x(), py + this.y())
        if (
          px + this.velocity.x > barrier.xy.x &&
          px + this.velocity.x < barrier.xy.x + barrier.width &&
          py + this.velocity.y > barrier.xy.y &&
          py + this.velocity.y < barrier.xy.y + barrier.height
        ) {
          let bVec = p.createVector(barrier.xy.x+(barrier.width/2), barrier.xy.y+(barrier.height/2))
          bVec.sub(this.location)

          let bAngle = p.abs(p.atan(bVec.y/bVec.x))

          if (bAngle >= p.PI/4 && bAngle < 3*p.PI/4) {
            // bottom
            this.velocity.y *= -1
            // this.location.y += 1
          } else if (bAngle >= 3*p.PI/4 && bAngle < 5*p.PI/4) {
            // left side
            this.velocity.x *= -1
            // this.location.x -= 1
          } else if (bAngle >= 5*p.PI/4 && bAngle < 7*p.PI/4) {
            // top
            this.velocity.y *= -1
            // this.location.y -= 1
          } else  {
            //right side
            this.velocity.x *= -1
            // this.location.x += 1
          }

          done = true;
        } else {this.color = 'blue'}
      }
    }
  }

  this.lookAtMouse = function() {
    let mouse = p.createVector(p.mouseX, p.mouseY);
    let newVector = p.createVector(mouse.x, mouse.y);
    newVector.sub(this.location);
    newVector.normalize();
    this.angle = newVector;
  }

  this.checkKeys = function() {
    //w
    if (p.keyIsDown(87)) {
      this.applyForce(p.createVector(0,-1))
    }

    //s
    if (p.keyIsDown(83)) {
      p.character.applyForce(p.createVector(0,1))
    }

    //d
    if (p.keyIsDown(68)) {
      p.character.applyForce(p.createVector(1,0))
    }

    //a
    if (p.keyIsDown(65)) {
      p.character.applyForce(p.createVector(-1,0))
    }

    //space
    if (p.keyIsDown(32)) {
      p.frameCount % 5 == 0 && p.character.shoot()
    }
  }

  this.update = function() {
    this.velocity.add(this.acceleration);
    this.location.add(this.velocity);
    this.acceleration.mult(0);
  }
}

